    return 1
  }
  echo "$output" | grep -q "Cargo.lock" || {
    echo "    error message did not mention Cargo.lock: $output" >&2
    return 1
  }
}

test_lockfile_uses_cargo_when_on_path() {
  # When cargo is on PATH, the script must take the cargo branch and not
  # fall through to the awk fallback. Detect via a sentinel file the stub
  # drops next to Cargo.lock — the awk path never touches that file.
  local dir
  dir=$(mktemp -d)
  trap "rm -rf '$dir'" RETURN
  build_fake_repo "$dir" "0.1.0"
  install_bump_script "$dir"
  write_changelog "$dir/CHANGELOG.md" "0.1.0" "### Fixed

- prior"

  local stubdir
  stubdir=$(install_cargo_stub "$dir")

  (cd "$dir" && PATH="$stubdir:$NOCARGO_PATH" ./scripts/bump-version.sh 0.2.0) > /dev/null 2>&1 || return 1

  # Stub left its sentinel — proves the cargo branch ran.
  [ -f "$dir/packages/desktop/src-tauri/.cargo-stub-ran" ] || {
    echo "    cargo stub sentinel missing — script fell through to awk fallback" >&2
    return 1
  }

  # And the lockfile is still consistent (stub uses the same awk edit).
  local lock="$dir/packages/desktop/src-tauri/Cargo.lock"
  awk '
    /^\[\[package\]\]/ { in_pkg = 1; is_target = 0; next }
    in_pkg && /^name = "chroxy-desktop"$/ { is_target = 1; next }
    in_pkg && is_target && /^version = "/ {
      sub(/^version = "/, ""); sub(/".*$/, ""); print; exit
    }
    /^$/ { in_pkg = 0; is_target = 0 }
  ' "$lock" | grep -qx "0.2.0"
}

