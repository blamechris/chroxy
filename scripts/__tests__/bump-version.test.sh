# --- Cargo.lock sync coverage (#4253) ---------------------------------------

test_lockfile_awk_rewrites_only_chroxy_desktop() {
  # The awk fallback must rewrite the `chroxy-desktop` stanza only — a
  # transitive dep that happens to share the same version string must stay
  # pinned to its original version.
  local dir
  dir=$(mktemp -d)
  trap "rm -rf '$dir'" RETURN
  build_fake_repo "$dir" "0.1.0"
  install_bump_script "$dir"
  write_changelog "$dir/CHANGELOG.md" "0.1.0" "### Fixed

- prior"

  (cd "$dir" && PATH="$NOCARGO_PATH" ./scripts/bump-version.sh 0.2.0) > /dev/null 2>&1 || return 1

  local lock="$dir/packages/desktop/src-tauri/Cargo.lock"

  # The chroxy-desktop stanza moved to 0.2.0.
  awk '
    /^\[\[package\]\]/ { in_pkg = 1; is_target = 0; next }
    in_pkg && /^name = "chroxy-desktop"$/ { is_target = 1; next }
    in_pkg && is_target && /^version = "/ {
      sub(/^version = "/, ""); sub(/".*$/, ""); print; exit
    }
    /^$/ { in_pkg = 0; is_target = 0 }
  ' "$lock" | grep -qx "0.2.0" || {
    echo "    chroxy-desktop stanza did not move to 0.2.0" >&2
    return 1
  }

  # The decoy stanza (same starting version, different name) stayed at 0.1.0
  # — a bare grep/sed on `version = "0.1.0"` would have rewritten this too.
  awk '
    /^\[\[package\]\]/ { in_pkg = 1; is_target = 0; next }
    in_pkg && /^name = "decoy-dep"$/ { is_target = 1; next }
    in_pkg && is_target && /^version = "/ {
      sub(/^version = "/, ""); sub(/".*$/, ""); print; exit
    }
    /^$/ { in_pkg = 0; is_target = 0 }
  ' "$lock" | grep -qx "0.1.0" || {
    echo "    decoy-dep stanza was incorrectly rewritten (false-match regression)" >&2
    return 1
  }
}

test_lockfile_verify_fails_when_stanza_missing() {
  # If Cargo.lock has no chroxy-desktop stanza (corruption, accidental
  # delete, untracked checkout), the post-verify must abort with a non-zero
  # exit — silent success would let CI ship a stale lockfile.
  local dir
  dir=$(mktemp -d)
  trap "rm -rf '$dir'" RETURN
  build_fake_repo "$dir" "0.1.0"
  install_bump_script "$dir"
  write_changelog "$dir/CHANGELOG.md" "0.1.0" "### Fixed

- prior"

  # Replace the seeded Cargo.lock with one missing the chroxy-desktop stanza.
  cat > "$dir/packages/desktop/src-tauri/Cargo.lock" <<'EOF'
# This file is automatically @generated by Cargo.
# It is not intended for manual editing.
