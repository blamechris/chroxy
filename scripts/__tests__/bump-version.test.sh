#!/usr/bin/env bash
#
# bump-version.test.sh — Self-contained test harness for scripts/bump-version.sh.
#
# Runs in a temp dir with a minimal fake monorepo skeleton (just the file paths
# bump-version.sh writes to). Each test case asserts an observable behavior of
# the script. No external test framework — keeps CI dep surface zero.
#
# Run from repo root:
#   bash scripts/__tests__/bump-version.test.sh
#
# Exit status: 0 if all tests pass, 1 otherwise.
#
set -uo pipefail

REPO_ROOT="$(cd "$(dirname "$0")/../.." && pwd)"
BUMP="$REPO_ROOT/scripts/bump-version.sh"

PASS=0
FAIL=0
FAILED_TESTS=()

# PATH stripped of cargo so the lockfile-sync block deterministically takes
# the awk-in-place fallback. Tests that need to exercise the `command -v
# cargo` branch install a stub via install_cargo_stub() and prepend its
# directory to this value.
#
# Computed by walking the inherited $PATH and dropping every entry that
# actually contains a `cargo` executable. The remaining entries still give
# the script `node`, `awk`, `grep`, `sed`, `date`, etc. — every other
# external tool bump-version.sh shells out to. A hardcoded list would have
# to track per-runner Node layouts (CI uses /opt/hostedtoolcache, devs use
# /opt/homebrew or volta, nix uses /nix/store, etc.).
__strip_cargo_from_path() {
  local p="$1"
  local out=""
  local IFS=":"
  for d in $p; do
    [ -n "$d" ] || continue
    [ -x "$d/cargo" ] && continue
    if [ -z "$out" ]; then
      out="$d"
    else
      out="$out:$d"
    fi
  done
  printf '%s\n' "$out"
}
NOCARGO_PATH="$(__strip_cargo_from_path "$PATH")"

# --- helpers -----------------------------------------------------------------

# Build a minimal fake repo at $1 that satisfies every path bump-version.sh
# touches. Versions all start at $2.
#
# Cargo.lock includes a realistic `chroxy-desktop` stanza AND a transitive
# dependency at the same version — the lockfile-sync block must only rewrite
# the workspace stanza, never the look-alike transitive. The harness reuses
# the same fixture so every successful-bump test indirectly guards against the
# false-match regression.
build_fake_repo() {
  local dir="$1"
  local v="$2"
  mkdir -p "$dir/packages/server" "$dir/packages/app/ios/Chroxy" \
           "$dir/packages/desktop/src-tauri" "$dir/packages/protocol" \
           "$dir/packages/store-core" "$dir/packages/dashboard" \
           "$dir/packages/claude-hooks" "$dir/packages/design-tokens"

  for pkg in server app desktop protocol store-core dashboard claude-hooks design-tokens; do
    printf '{"name":"%s","version":"%s"}\n' "$pkg" "$v" \
      > "$dir/packages/$pkg/package.json"
  done
  printf '{"name":"chroxy","version":"%s"}\n' "$v" > "$dir/package.json"
  printf '{"expo":{"name":"chroxy","version":"%s"}}\n' "$v" \
    > "$dir/packages/app/app.json"
  printf '{"version":"%s"}\n' "$v" \
    > "$dir/packages/desktop/src-tauri/tauri.conf.json"
  cat > "$dir/packages/desktop/src-tauri/Cargo.toml" <<EOF
[package]
name = "chroxy-desktop"
version = "$v"
edition = "2021"
EOF
  cat > "$dir/packages/app/ios/Chroxy/Info.plist" <<EOF
<?xml version="1.0" encoding="UTF-8"?>
<plist version="1.0">
<dict>
  <key>CFBundleShortVersionString</key>
  <string>$v</string>
</dict>
</plist>
EOF
  printf '{"version":"%s","packages":{"":{"version":"%s"}}}\n' "$v" "$v" \
    > "$dir/package-lock.json"
  printf '{"version":"%s","packages":{"":{"version":"%s"}}}\n' "$v" "$v" \
    > "$dir/packages/server/package-lock.json"

  # Minimal Cargo.lock mirroring the shape of the real one. The decoy stanza
  # is a synthetic transitive at the same version — the lockfile-sync block
  # must scope its rewrite to the `chroxy-desktop` stanza only.
  cat > "$dir/packages/desktop/src-tauri/Cargo.lock" <<EOF
# This file is automatically @generated by Cargo.
# It is not intended for manual editing.
version = 4

[[package]]
name = "decoy-dep"
version = "$v"
source = "registry+https://github.com/rust-lang/crates.io-index"
checksum = "deadbeef"

[[package]]
name = "chroxy-desktop"
version = "$v"
dependencies = [
 "serde",
 "tauri",
]

[[package]]
name = "serde"
version = "1.0.0"
source = "registry+https://github.com/rust-lang/crates.io-index"
checksum = "cafebabe"
EOF
}

# Copy bump-version.sh into the fake repo. No source mutation — the script
# runs as-is, and PATH control (via NOCARGO_PATH / install_cargo_stub) picks
# which branch of the lockfile-sync block is exercised.
install_bump_script() {
  local dir="$1"
  mkdir -p "$dir/scripts"
  cp "$BUMP" "$dir/scripts/bump-version.sh"
  chmod +x "$dir/scripts/bump-version.sh"
}

# Install a `cargo` shim that performs the same in-place rewrite the awk
# fallback would, and drops a sentinel file so tests can assert the cargo
# branch was actually taken (i.e., the script didn't fall through to awk).
# Returns the directory holding the shim — caller is responsible for putting
# it on $PATH before running the bump.
install_cargo_stub() {
  local dir="$1"
  local stubdir="$dir/.stub-bin"
  mkdir -p "$stubdir"
  cat > "$stubdir/cargo" <<'STUB'
#!/usr/bin/env bash
# Minimal cargo stub: parse --precise <V> from argv, rewrite the
# chroxy-desktop stanza in ./Cargo.lock, drop a sentinel so the test can
# prove this branch ran instead of the awk fallback.
set -euo pipefail
new=""
while [ $# -gt 0 ]; do
  case "$1" in
    --precise) new="$2"; shift 2 ;;
    *) shift ;;
  esac
done
[ -n "$new" ] || { echo "cargo stub: missing --precise" >&2; exit 1; }
: > "$PWD/.cargo-stub-ran"
tmp="$PWD/Cargo.lock.stub.tmp"
awk -v new="$new" '
  /^\[\[package\]\]/ { in_pkg = 1; is_target = 0 }
  in_pkg && /^name = "chroxy-desktop"$/ { is_target = 1 }
  in_pkg && is_target && /^version = "/ {
    sub(/^version = "[^"]*"/, "version = \"" new "\"")
    is_target = 0
  }
  /^$/ { in_pkg = 0; is_target = 0 }
  { print }
' "$PWD/Cargo.lock" > "$tmp" && mv "$tmp" "$PWD/Cargo.lock"
STUB
  chmod +x "$stubdir/cargo"
  echo "$stubdir"
}

# Write a minimal CHANGELOG.md (header + one prior section).
write_changelog() {
  local file="$1"
  local prev_version="$2"
  local prev_body="$3"
  cat > "$file" <<EOF
# Changelog

All notable changes to this project will be documented in this file.

The format is based on [Keep a Changelog](https://keepachangelog.com/en/1.1.0/),
and this project adheres to [Semantic Versioning](https://semver.org/spec/v2.0.0.html).

## [$prev_version] - 2026-01-01

$prev_body
EOF
}

run_test() {
  local name="$1"
  shift
  if "$@"; then
    PASS=$((PASS + 1))
    echo "  PASS  $name"
  else
    FAIL=$((FAIL + 1))
    FAILED_TESTS+=("$name")
    echo "  FAIL  $name"
  fi
}

# --- test cases --------------------------------------------------------------

test_scaffolds_new_section() {
  local dir
  dir=$(mktemp -d)
  trap "rm -rf '$dir'" RETURN
  build_fake_repo "$dir" "0.1.0"
  install_bump_script "$dir"
  write_changelog "$dir/CHANGELOG.md" "0.1.0" "### Fixed

- Some prior fix (#1)"

  (cd "$dir" && PATH="$NOCARGO_PATH" ./scripts/bump-version.sh 0.2.0) > /dev/null 2>&1 || return 1

  # New section header present, on its own line, with today's date
  local today
  today=$(date +%Y-%m-%d)
  grep -q "^## \[0.2.0\] - $today\$" "$dir/CHANGELOG.md" || {
    echo "    expected: ## [0.2.0] - $today" >&2
    return 1
  }

  # All three placeholder sections present
  grep -q "^### Added\$" "$dir/CHANGELOG.md" || return 1
  grep -q "^### Changed\$" "$dir/CHANGELOG.md" || return 1
  grep -q "^### Fixed\$" "$dir/CHANGELOG.md" || return 1

  # TODO markers present (drift guard fodder)
  [ "$(grep -c '^- TODO:' "$dir/CHANGELOG.md")" -eq 3 ] || return 1

  # Prior section preserved
  grep -q "^## \[0.1.0\] - 2026-01-01" "$dir/CHANGELOG.md" || return 1
  grep -q "Some prior fix" "$dir/CHANGELOG.md" || return 1

  # New section appears BEFORE the prior section (Keep-a-Changelog ordering)
  local new_line prev_line
  new_line=$(grep -n "^## \[0.2.0\]" "$dir/CHANGELOG.md" | head -1 | cut -d: -f1)
  prev_line=$(grep -n "^## \[0.1.0\]" "$dir/CHANGELOG.md" | head -1 | cut -d: -f1)
  [ "$new_line" -lt "$prev_line" ]
}

test_inserts_after_unreleased_section() {
  # #5207 — when the CHANGELOG has an [Unreleased] section on top, the new
  # versioned section must land directly BELOW it (and its body), not above —
  # preserving Keep-a-Changelog ordering and not burying pending entries.
  local dir
  dir=$(mktemp -d)
  trap "rm -rf '$dir'" RETURN
  build_fake_repo "$dir" "0.1.0"
  install_bump_script "$dir"
  cat > "$dir/CHANGELOG.md" <<'EOF'
# Changelog

The format is based on [Keep a Changelog]; [Unreleased] stays on top.

## [Unreleased]

### Added

- A pending entry not yet released

## [0.1.0] - 2026-01-01

### Added

- Initial release
EOF

  (cd "$dir" && PATH="$NOCARGO_PATH" ./scripts/bump-version.sh 0.2.0) > /dev/null 2>&1 || return 1

  grep -q "^## \[0.2.0\] - " "$dir/CHANGELOG.md" || return 1

  # Ordering must be: [Unreleased] < [0.2.0] < [0.1.0]
  local unrel_line new_line prev_line
  unrel_line=$(grep -n "^## \[Unreleased\]" "$dir/CHANGELOG.md" | head -1 | cut -d: -f1)
  new_line=$(grep -n "^## \[0.2.0\]" "$dir/CHANGELOG.md" | head -1 | cut -d: -f1)
  prev_line=$(grep -n "^## \[0.1.0\]" "$dir/CHANGELOG.md" | head -1 | cut -d: -f1)
  [ "$unrel_line" -lt "$new_line" ] || {
    echo "    new section landed above [Unreleased] (line $new_line vs $unrel_line)" >&2
    return 1
  }
  [ "$new_line" -lt "$prev_line" ] || {
    echo "    new section landed below the prior release (line $new_line vs $prev_line)" >&2
    return 1
  }

  # The pending [Unreleased] entry must be preserved (not buried/dropped) AND
  # stay above the new version header — i.e. the new section must land below the
  # [Unreleased] *body*, not immediately after the `## [Unreleased]` line (which
  # would push the pending entry under the cut release). Assert by line number,
  # not just existence, so a regression that splices after the header line fails.
  local pending_line
  pending_line=$(grep -n "A pending entry not yet released" "$dir/CHANGELOG.md" | head -1 | cut -d: -f1)
  [ -n "$pending_line" ] || {
    echo "    pending [Unreleased] entry was dropped" >&2
    return 1
  }
  [ "$pending_line" -lt "$new_line" ] || {
    echo "    pending [Unreleased] entry landed below the new section (line $pending_line vs $new_line)" >&2
    return 1
  }
}

