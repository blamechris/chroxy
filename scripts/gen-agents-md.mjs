#!/usr/bin/env node
// gen-agents-md.mjs — generate AGENTS.md from CLAUDE.md (the single source of truth).
//
// Claude Code is the primary driver in this repo and loads CLAUDE.md directly.
// Agents that do NOT read CLAUDE.md (Codex, Gemini, and other tools that look
// for AGENTS.md) need the same guidance — so AGENTS.md is a generated mirror of
// CLAUDE.md, never hand-edited. Edit CLAUDE.md, then regenerate:
//
//   node scripts/gen-agents-md.mjs           # write AGENTS.md
//   node scripts/gen-agents-md.mjs --check   # exit 1 if AGENTS.md is stale (CI gate)
//
// Keeping CLAUDE.md as the ONE source and generating AGENTS.md means the two can
// never drift and a convention can never be silently dropped from the mirror.

import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
const CLAUDE_PATH = join(ROOT, 'CLAUDE.md')
const AGENTS_PATH = join(ROOT, 'AGENTS.md')

const HEADER = `<!--
  AUTO-GENERATED FROM CLAUDE.md — DO NOT EDIT THIS FILE BY HAND.
  Regenerate with:  node scripts/gen-agents-md.mjs
  CI (Scripts Tests) fails if AGENTS.md drifts from CLAUDE.md.
-->

# Agent Instructions

> **This file is generated from [\`CLAUDE.md\`](CLAUDE.md) — the single source of truth.**
> Claude Code is the primary driver in this repo and loads \`CLAUDE.md\` directly; this
> mirror exists so agents that do **not** read \`CLAUDE.md\` (Codex, Gemini, and other tools
> that look for \`AGENTS.md\`) get the exact same project guidance. **Do not edit AGENTS.md** —
> edit \`CLAUDE.md\` and run \`node scripts/gen-agents-md.mjs\`.
>
> References below to "Claude", the \`claude\` CLI, or the \`.claude/\` directory describe the
> Claude Code setup specifically — apply the equivalent for your own agent and tooling.

---

`

// Render the canonical AGENTS.md contents from CLAUDE.md.
export function renderAgentsMd(claudeContents) {
  return HEADER + claudeContents
}

export function readClaudeMd() {
  return readFileSync(CLAUDE_PATH, 'utf8')
}

export function readAgentsMd() {
  try {
    return readFileSync(AGENTS_PATH, 'utf8')
  } catch {
    return null
  }
}

// Only run the CLI side-effects when invoked directly (not when imported by tests).
if (process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1]) {
  const expected = renderAgentsMd(readClaudeMd())
  if (process.argv.includes('--check')) {
    if (readAgentsMd() !== expected) {
      console.error('::error::AGENTS.md is out of sync with CLAUDE.md. Run `node scripts/gen-agents-md.mjs` and commit AGENTS.md.')
      process.exit(1)
    }
    console.log('AGENTS.md is in sync with CLAUDE.md.')
  } else {
    writeFileSync(AGENTS_PATH, expected)
    console.log('Wrote AGENTS.md from CLAUDE.md.')
  }
}
